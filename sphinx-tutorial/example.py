def greet(name):
    """
    This function greets the given name.
    
    :param name: The name to greet.
    :type name: str
    :return: A greeting message.
    :rtype: str
    """
    return f"Hello, {name}!"


def add(x, y):
    """
    This function adds two numbers.
    
    :param x: The first number.
    :type x: int
    :param y: The second number.
    :type y: int
    :return: The sum of x and y.
    :rtype: int
    """
    return x + y

